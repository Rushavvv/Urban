    private void loadListToTable(){
            DefaultTableModel model = (DefaultTableModel) camTable.getModel();

            model.setRowCount(0);
            
            camList.forEach(camera -> model.addRow(new Object[]{
                camera.getId(),
                camera.getName(),
                camera.getPrice(),
                camera.getStock(),
                camera.getResolution()
            }));
    }
    
